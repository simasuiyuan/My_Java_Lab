public class Main {
    public static void main(String[] args) throws Exception {
        for(int i=0; i<=10; i++) {
            System.out.println(i);
        }
        for(int i=10; i>=0; i--) {
            System.out.println(i);
        }
        System.out.println("Happy new year");
        for(int i=10; i>=0; ) {
            System.out.println(i);
            i-=2;
        }
        System.out.println("hello!");
    }
}
